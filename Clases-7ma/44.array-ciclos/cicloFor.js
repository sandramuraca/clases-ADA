/*-----CICLO FOR-------- */
//Se utiliza para repetir una determinada candidad de veces, tareas o acciones

//Estructura => fir(inicio,final, incremento){codigo a ejecutar}
// es muy importante indicar el final para que no sea un loop infinito

//con esta indicacion le digo que vaya del 1 al 100 (i en la primera vuelta vale 1 y si le sumo uno en la segunda vuelta vale 2 y asi se repite hata el final, que en este caso es 20)

for(i = 0;  i <= 20; i++) {
    console.log(i)
}

for(i = 1; i < 30; i += 2){
    console.log(i)
}